async function openGameMenu(page) {
  const menu = page.locator('#mainNavMenu');
  await expect(menu.locator('summary')).toBeVisible();
  const isOpen = await menu.evaluate((node) => Boolean(node.open));
  if (!isOpen) await menu.locator('summary').click();
  await expect(menu.getByRole('button', { name: 'WORK', exact: true })).toBeVisible();
}

async function clickMenuTab(page, name) {
  await openGameMenu(page);
  await page.locator('#mainNavMenu').getByRole('button', { name, exact: true }).click();
}

