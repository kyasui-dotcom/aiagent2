async function bootstrap() {
  await refreshAuthSnapshot();
  applyInboundContext(await fetchCaitAppContextFromUrl());
  render();
  void refreshGithubRepos({ silent: true });
}

void bootstrap();

function escapeHtml(value = '') {
  return String(value ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
